#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	size_t	size;
	char	*str;

	i = 0;
	if (!s || !f)
		return (NULL);
	size = ft_strlen((char *)s);
	if (!(str = (char *)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
